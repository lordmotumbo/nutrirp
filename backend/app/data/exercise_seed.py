def build_seed_data():
    """Constrói a lista de exercícios com URLs de thumbnail e video montadas."""
    seed = []
    for ex in EXERCISES:
        gif_id = ex.get("gif_id", "")
        if gif_id:
            thumbnail = f"{BASE_GIF_URL}/{gif_id}/0.jpg"
            video_url = f"{BASE_GIF_URL}/{gif_id}/1.jpg"
        else:
            thumbnail = PLACEHOLDER_IMG
            video_url = None

        seed.append({
            "name": ex["name"],
            "description": ex.get("description", ""),
            "muscle_group": ex.get("muscle_group", ""),
            "subgroup": ex.get("subgroup", ""),
            "difficulty": ex.get("difficulty", "iniciante"),
            "equipment": ex.get("equipment", ""),
            "category": ex.get("category", "strength"),
            "thumbnail": thumbnail,
            "video_url": video_url,
        })
    return seed
